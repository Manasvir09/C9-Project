
function setup() {
  createCanvas(400,400);
}

function draw() 
{
  

  if(keyIsDown(RIGHT_ARROW)){

    background("pink");

  }

  if(keyIsDown(LEFT_ARROW)){

    background("yellow");

  }
 
  if(keyIsDown(UP_ARROW)){

    background("lavender");

  }

  if(keyIsDown(DOWN_ARROW)){

    background("magenta");

  }
}




